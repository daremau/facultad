num_a = int(input("Ingresar numero A: "))
num_b = int(input("Ingresar numero B: "))
num_c = int(input("Ingresar numero C: "))
num_d = int(input("Ingresar numero D: "))

suma = num_a + num_b + num_c + num_d
producto = num_a * num_b * num_c * num_d
promedio = suma/4

print(f'Los numeros son: {num_a}, {num_b}, {num_c}, {num_d}\n'
      f'La suma es {suma}, el producto {producto}, y el promedio {promedio}')